// Типы данных приведенные к трем типам: Number, String, Boolean.

let age = 25; // Number
let eat = 'potato'; // String
let isFishSwimming = true; // Boolean
let bigint = 4356432175321569n; // Bigint
let color = null; // Null
let name; // Undefined
let user = new Object(); // Object
let sym = Symbol(); // Symbol


console.log(Number(age), String(age), Boolean(age));
console.log(Number(eat), String(eat), Boolean(eat));
console.log(Number(isFishSwimming), String(isFishSwimming), Boolean(isFishSwimming));
console.log(Number(bigint), String(bigint), Boolean(bigint));
console.log(Number(color), String(color), Boolean(color));
console.log(Number(name), String(name), Boolean(name));
console.log(Number(user), String(user), Boolean(user));
console.log(Number(sym), String(sym), Boolean(sym));


